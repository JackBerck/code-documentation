// 1. && (and/dan)
// 2. || (or/atau)
// 3. ! (not/tidak)

// kalau and/dan semua ekspresinya harus benar(semua valuenya harus benar), sedangkan or/atau salah satu saja sudah benar(salah satu value benar), dan not/tidak penulisannya harus didepan untuk membalikkan nilai false menjadi true atau sebaliknya