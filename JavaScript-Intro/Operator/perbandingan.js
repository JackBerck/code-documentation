// 3. === (strict sama dengan)
// 4. !== (strict tidak sama dengan)
// 5. > (lebih besar dari)
// 6. < (lebih kecil dari)
// 7. >= (lebih besar sama dengan dari)
// 7. <= (lebih kecil sama dengan dari)

// yang nantinya dari perbandingan tersebut akan menghasilkan tipe data boolean yang di dalamnya berupa nilai true(jika benar) atau false(jika salah)

