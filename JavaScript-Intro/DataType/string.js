// tipe data string digunakan untuk mempresentasikan data tekstual(biasa disebut plain text)

// cara menggunakan string yaitu dengan cara membungkus data atau nilai kita dengan memakai tanda kutip '' atau "" terserah mau mana saja.
// 'Zaki' "Dzulfikar"

// di dalam string juga memiliki escape character yang digunakan untuk menampilkan sebuah character yang diinterpretasikan berbeda dalam sebuah bahasa pemprograman, ada beberapa escape character, yaitu:
// \0 menghasilkan karakter null
// \' menghasilkan karakter '
// \" menghasilkan karakter "
// \\ menghasilkan karakter \
// \n menambahkan baris baru (new line)
// \t menambahkan tab
// \b menambahkan backspace
// \uXXXX menampilkan unicode
// misal => "Hari ini hari \"Sumpah Pemuda\" yang bertepatan pada hari Jum\'at"

// kita juga dapat menambahkan string baru agar nantinya bisa tersambung atau biasa disebut konkatenasi dengan menambahkan + maka akan menggabungkan string
// 'Zaki ' + "Dzulfikar"

// kita juga dapat membandingkan string dengan menambahkan == yang akan menghasilkan nilai boolean true/false (saat melakukan perbandingan besar kecil karakter berpengaruh)
// 'Zaki' == "zaki"

// string juga memiliki fungsi yaiti .length. .length ini digunakan untuk menghitung panjang string
// 'Zaki Dzulfikar'.length